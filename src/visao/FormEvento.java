    private void jTableEventoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEventoMouseClicked
        // TODO add your handling code here:
        String nome = "" + jTableEvento.getValueAt(jTableEvento.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSQL("select *from evento where nome ='" + nome + "'");
        try {
            conex.rs.first();
            jTextFieldID.setText(String.valueOf(conex.rs.getInt("idevento")));
            jTextFieldNome.setText(conex.rs.getString("nome"));
            jTextFieldAno.setText(conex.rs.getString("ano"));
            jDateChooserInicio.setDate(conex.rs.getDate("dtInicio"));
            jDateChooserFim.setDate(conex.rs.getDate("dtTermino"));
            jTextFieldQtMinMenbro.setText(conex.rs.getString("qtMinMenbro"));
            jTextFieldQtMaxMenbro.setText(conex.rs.getString("qtMaxMenbro"));
            jTextFieldNotaEvento.setText(conex.rs.getString("nota"));
            jTextFieldQtMinAvalia.setText(conex.rs.getString("qtAvaliador"));
            jTextFieldCriterio.setText(conex.rs.getString("criterio"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados");
        }
        conex.desconecta();
        jTextFieldNome.setEnabled(false);
        jTextFieldAno.setEnabled(false);
        jDateChooserInicio.setEnabled(false);
        jDateChooserFim.setEnabled(false);
        jTextFieldQtMinMenbro.setEnabled(false);
        jTextFieldQtMaxMenbro.setEnabled(false);
        jTextFieldNotaEvento.setEnabled(false);
        jTextFieldQtMinAvalia.setEnabled(false);
        jTextFieldCriterio.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonNovo.setEnabled(false);
        jButtonCancelar.setEnabled(true);
    }//GEN-LAST:event_jTableEventoMouseClicked

