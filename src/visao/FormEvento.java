    public void preencherTable(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "NOME", "ANO", "DTINICIO", "DTTERMINO", "QTMINMENBRO", "QTMAXMENBRO", "NOTA", "QTAVALIADOR", "CRITERIO"};
        conex.conexao();
        conex.executaSQL(sql);

        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getInt("idevento"), conex.rs.getString("nome"), conex.rs.getString("ano"),
                    conex.rs.getString("dtInicio"), conex.rs.getString("dtTermino"), conex.rs.getString("qtMinMenbro"), conex.rs.getString("qtMaxMenbro"),
                    conex.rs.getString("nota"), conex.rs.getString("qtAvaliador"), conex.rs.getString("criterio")});
            } while (conex.rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possível fazer o preenchimento");
        }
        BeansTabela modelo = new BeansTabela(dados, colunas);
        jTableEvento.setModel(modelo);
        jTableEvento.getColumnModel().getColumn(0).setPreferredWidth(25);
        jTableEvento.getColumnModel().getColumn(0).setResizable(false);
        jTableEvento.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableEvento.getColumnModel().getColumn(1).setResizable(false);
        jTableEvento.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableEvento.getColumnModel().getColumn(2).setResizable(false);
        jTableEvento.getColumnModel().getColumn(3).setPreferredWidth(200);
        jTableEvento.getColumnModel().getColumn(3).setResizable(false);
        jTableEvento.getColumnModel().getColumn(4).setPreferredWidth(123);
        jTableEvento.getColumnModel().getColumn(4).setResizable(false);
        jTableEvento.getColumnModel().getColumn(0).setPreferredWidth(25);
        jTableEvento.getColumnModel().getColumn(0).setResizable(false);
        jTableEvento.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableEvento.getColumnModel().getColumn(1).setResizable(false);
        jTableEvento.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableEvento.getColumnModel().getColumn(2).setResizable(false);
        jTableEvento.getColumnModel().getColumn(3).setPreferredWidth(200);
        jTableEvento.getColumnModel().getColumn(3).setResizable(false);
        jTableEvento.getColumnModel().getColumn(4).setPreferredWidth(123);
        jTableEvento.getColumnModel().getColumn(4).setResizable(false);
        jTableEvento.getTableHeader().setReorderingAllowed(false);
        jTableEvento.setAutoResizeMode(jTableEvento.AUTO_RESIZE_OFF);
        jTableEvento.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conex.desconecta();
    }

