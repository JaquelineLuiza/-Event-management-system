    private void jTableVinculacaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableVinculacaoMouseClicked
        // TODO add your handling code here:
        String nome = "" + jTableVinculacao.getValueAt(jTableVinculacao.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSQL("select *from avaliador_trabalho where pessoa_avaliador like'%" + nome + "%'");
        try {
            conex.rs.first();
            jTextFieldID.setText(String.valueOf(conex.rs.getInt("idavaliador_trabalho")));
            buscaPessoaNome(conex.rs.getInt("pessoa_avaliador"));
            jComboBoxAvaliadores.setSelectedItem(nomePessoa);
            buscaTrabalhoNome(conex.rs.getInt("trabalho_avaliar"));
            jComboBoxTrabalhos.setSelectedItem(nomeTrabalho);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados" + ex);
        }
        conex.desconecta();
        jTextFieldPesquisa.setText("");
        jTextFieldPesquisa.setEnabled(false);
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonNovo.setEnabled(false);
        jButtonCancelar.setEnabled(true);
    }//GEN-LAST:event_jTableVinculacaoMouseClicked

    public void preencherTable(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "AVALIADOR", "TRABALHO"};
        conex.conexao();
        conex.executaSQL(sql);
        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getInt("idavaliador_trabalho"), conex.rs.getInt("pessoa_avaliador"), conex.rs.getInt("trabalho_avaliar")});
            } while (conex.rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possível fazer o preenchimento" + e);
        }
        BeansTabela modelo = new BeansTabela(dados, colunas);
        jTableVinculacao.setModel(modelo);
        jTableVinculacao.getColumnModel().getColumn(0).setPreferredWidth(25);
        jTableVinculacao.getColumnModel().getColumn(0).setResizable(false);
        jTableVinculacao.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableVinculacao.getColumnModel().getColumn(1).setResizable(false);
        jTableVinculacao.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableVinculacao.getColumnModel().getColumn(2).setResizable(false);

        jTableVinculacao.getTableHeader().setReorderingAllowed(false);
        jTableVinculacao.setAutoResizeMode(jTableVinculacao.AUTO_RESIZE_OFF);
        jTableVinculacao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conex.desconecta();
    }

