    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (jComboBoxTrabalhos.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Escolha o trabalho para prosseguir");
            jComboBoxTrabalhos.requestFocus();
        } else if (jComboBoxAvaliadores.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Escolha o avaliador para prosseguir");
            jComboBoxAvaliadores.requestFocus();
        } else if (flag == 1) {
            mod.setAvaliador((String) jComboBoxAvaliadores.getSelectedItem());
            mod.setTrabalho((String) jComboBoxTrabalhos.getSelectedItem());
            controlAvalidorTrabalho.salvar(mod);

            jTextFieldID.setText("");
            jComboBoxAvaliadores.setSelectedItem(null);
            jComboBoxTrabalhos.setSelectedItem(null);
            jTextFieldPesquisa.setText("");

            jComboBoxAvaliadores.setEnabled(false);
            jComboBoxTrabalhos.setEnabled(false);
            jListVinculacao.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            jTextFieldPesquisa.setEnabled(false);
            jButtonpesquisar.setEnabled(false);
            preencherTable("select *from avaliador_trabalho ORDER BY idavaliador_trabalho");

        } else {
            mod.setAvaliador((String) jComboBoxAvaliadores.getSelectedItem());
            mod.setTrabalho((String) jComboBoxTrabalhos.getSelectedItem());
            controlAvalidorTrabalho.EditarVinculação(mod);

            jTextFieldID.setText("");
            jComboBoxAvaliadores.setSelectedItem(null);
            jComboBoxTrabalhos.setSelectedItem(null);
            jTextFieldPesquisa.setText("");

            jComboBoxAvaliadores.setEnabled(false);
            jComboBoxTrabalhos.setEnabled(false);
            jListVinculacao.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            jTextFieldPesquisa.setEnabled(false);
            jButtonpesquisar.setEnabled(false);
            preencherTable("select *from avaliador_trabalho ORDER BY idavaliador_trabalho");

        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

