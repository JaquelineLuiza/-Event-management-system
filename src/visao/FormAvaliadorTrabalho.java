    public void preencherPessoa() {
        conex.conexao();
        conex.executaSQL("select nome from pessoa");

        try {
            conex.rs.first();
            jComboBoxAvaliadores.removeAllItems();
            do {
                jComboBoxAvaliadores.addItem(conex.rs.getString("nome"));
            } while (conex.rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possível fazer o preenchimento" + e);
        }
        conex.desconecta();
    }

    public void preencherTrabalho() {
        conex.conexao();
        conex.executaSQL("select titulo from trabalho");

        try {
            conex.rs.first();
            jComboBoxTrabalhos.removeAllItems();
            do {
                jComboBoxTrabalhos.addItem(conex.rs.getString("titulo"));
            } while (conex.rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possível fazer o preenchimento" + e);
        }
        conex.desconecta();
    }
    public void buscaPessoaNome(int idPessoa) {
        conex.conexao();
        conex.executaSQL("select *from pessoa where idpessoa like'%" + idPessoa + "%'");
        try {
            conex.rs.first();
            nomePessoa = conex.rs.getString("nome");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Pessoa não cadastrada!");
        }
    }
    public void buscaTrabalhoNome(int idTrabalho) {
        conex.conexao();
        conex.executaSQL("select *from trabalho where idtrabalho like'%" + idTrabalho + "%'");
        try {
            conex.rs.first();
            nomeTrabalho = conex.rs.getString("titulo");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Trabalho não cadastrado!");
        }
    }

